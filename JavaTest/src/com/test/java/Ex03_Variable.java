package com.test.java;

public class Ex03_Variable {

	public static void main(String[] args) {
		/*
		변수, Variable
		- 개발자가 명령어를 사용해서 메모리 할당받은 공간
		- 목적 : 원하는 데이터를 읽거나 쓰기위한 공간
		
		변수
		1. 생성하기
		- 자료형 변수명;
			byte kor;
		
		2. 초기화 하기
		- 변수 = 값;
			kor = 100;
		 
		 3. 사용하기
		 System.out.println(kor);
		*/
		
		byte kor;
		kor = 100;
		System.out.println(kor);
	}

}
